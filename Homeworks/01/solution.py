def what_is_my_sign(day, month):
    SIGNS = [(120, 'Козирог'), (219, 'Водолей'), (321, 'Риби'),
             (421, 'Овен'), (521, 'Телец'), (621, 'Близнаци'),
             (722, 'Рак'), (823, 'Лъв'), (923, 'Дева'),
             (1023, 'Везни'), (1122, 'Скорпион'), (1222, 'Стрелец'),
             (1232, 'Козирог')]

    month_day = int("".join((str(month), '%02d' % day)))
    for sign in SIGNS:
        if month_day < sign[0]:
            return sign[1]
